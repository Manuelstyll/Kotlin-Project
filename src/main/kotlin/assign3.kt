fun main(){
    sum(23, 2)
    diff(13,7)
    multiplication(5,5)
    quotient(12,4)
}
fun sum(num1:Int,num2:Int){
    println(num1+num2)

}
fun diff(num1:Int,num2:Int){
    println(num1-num2)
}
